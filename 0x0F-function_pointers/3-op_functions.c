#include "calc.h"

/**
 * op_add - func that adds two numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a+b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - func that sutract two numbers
 * @a: 1st num
 * @b: 2nd num
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - func that multiplies two numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - func that divides two numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - func that gives the remainder from the division of two nums
 * @a: 1st number
 * @b: 2nd number
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
